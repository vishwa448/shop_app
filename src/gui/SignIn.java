    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String email = jTextField1.getText();
        String password = String.valueOf(jPasswordField1.getPassword());

        if (email.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter your email", "Warning", JOptionPane.WARNING_MESSAGE);
       
        } else if(!email.matches("^(?=.{1,64}@)[A-Za-z0-9_-]+(\\.[A-Za-z0-9_-]+)*@" 
        + "[^-][A-Za-z0-9-]+(\\.[A-Za-z0-9-]+)*(\\.[A-Za-z]{2,})$")){
            JOptionPane.showMessageDialog(this, "Invalid email", "Warning", JOptionPane.WARNING_MESSAGE);
  
        
        } else if (password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please passowrd your email", "Warning", JOptionPane.WARNING_MESSAGE);

        } else {
            
            try {
                
                //search input email on database 
                ResultSet resultSet = MySQL2.executeSearch("SELECT * FROM `employee` WHERE `email` = '"+email+"' AND `password` ='"+password+"' ");
           
                if (resultSet.next()) {
                    
                    String fName = resultSet.getString("first_name");
                    String lName = resultSet.getString("last_name");
                    
                    
                    //Open home page and close signin page 
                    Home home = new Home(email,fName,lName);
                    home.setVisible(true);
                    this.dispose();
                    
//                    System.out.println("Success");
                }else{
                JOptionPane.showMessageDialog(this,"Invalid email or password ", "Warning",JOptionPane.WARNING_MESSAGE);
                
                }
            
            } catch (Exception e) {
                e.printStackTrace();
                
            }
            
            
//            System.out.println("OK");

        }


    }//GEN-LAST:event_jButton1ActionPerformed

